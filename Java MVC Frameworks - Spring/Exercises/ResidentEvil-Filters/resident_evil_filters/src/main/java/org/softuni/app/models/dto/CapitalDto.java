package org.softuni.app.models.dto;

public class CapitalDto {

    private long id;

    private String name;

    public CapitalDto() {
    }

    public long getId() {
        return this.id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
